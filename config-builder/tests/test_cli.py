class TestValidate:
    def test_clean_cfg_returns_success(
        self, tmp_path: Path, capsys: pytest.CaptureFixture[str],
    ) -> None:
        # Generate a clean cfg first, then validate it.
        cfg = tmp_path / "server.cfg"
        cli.main(["generate", "--profile", "cup", "-o", str(cfg)])
        capsys.readouterr()                       # discard generate output

        rc = cli.main(["validate", str(cfg)])
        captured = capsys.readouterr()
        assert rc == cli.EXIT_SUCCESS
        assert "OK" in captured.out

    def test_dirty_cfg_returns_validation_exit(
        self, tmp_path: Path, capsys: pytest.CaptureFixture[str],
    ) -> None:
        cfg = tmp_path / "broken.cfg"
        cfg.write_text('seta sv_fps "9999"\n', encoding="utf-8")

        rc = cli.main(["validate", str(cfg)])
        captured = capsys.readouterr()
        assert rc == cli.EXIT_VALIDATION
        assert "out of range" in captured.out

    def test_json_format_is_parseable(
        self, tmp_path: Path, capsys: pytest.CaptureFixture[str],
    ) -> None:
        cfg = tmp_path / "broken.cfg"
        cfg.write_text('seta sv_fps "9999"\n', encoding="utf-8")

        rc = cli.main(["validate", str(cfg), "--format", "json"])
        assert rc == cli.EXIT_VALIDATION
        captured = capsys.readouterr()
        report = json.loads(captured.out)
        assert report["valid"] is False
        assert isinstance(report["issues"], list)
        assert any("out of range" in i for i in report["issues"])

    def test_missing_file_returns_io_error(
        self, tmp_path: Path, capsys: pytest.CaptureFixture[str],
    ) -> None:
        rc = cli.main(["validate", str(tmp_path / "nope.cfg")])
        assert rc == cli.EXIT_IO


