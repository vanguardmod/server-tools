class TestListProfiles:
    def test_lists_three_user_visible(
        self, capsys: pytest.CaptureFixture[str],
    ) -> None:
        rc = cli.main(["list-profiles"])
        assert rc == cli.EXIT_SUCCESS
        out = capsys.readouterr().out
        # User-visible names appear in the leading stem column...
        stem_lines = [
            ln for ln in out.splitlines()
            if ln and not ln.startswith(" ")
        ]
        stems = {ln.split()[0] for ln in stem_lines}
        assert stems == {"cup", "public", "scrim"}
        # ..._base may still appear inside the chain caption (it's the
        # parent of every profile), but never as a top-level stem.
        assert "_base" not in stems


# ---------------------------------------------------------------------------
# list-cvars
# ---------------------------------------------------------------------------
class TestListCvars:
    def test_lists_all_24_in_text_mode(
        self, capsys: pytest.CaptureFixture[str],
    ) -> None:
        rc = cli.main(["list-cvars"])
        assert rc == cli.EXIT_SUCCESS
        out = capsys.readouterr().out
        # 24 cvar rows; each non-empty line starts with the cvar name.
        cvar_lines = [ln for ln in out.splitlines() if ln and not ln.startswith(" ")]
        assert len(cvar_lines) == 24

    def test_category_filter(
        self, capsys: pytest.CaptureFixture[str],
    ) -> None:
        rc = cli.main(["list-cvars", "--category", "network"])
        assert rc == cli.EXIT_SUCCESS
        out = capsys.readouterr().out
        # Network cvars should be present, identity ones absent.
        assert "sv_fps" in out
        assert "sv_hostname" not in out

    def test_unknown_category_returns_success_with_message(
        self, capsys: pytest.CaptureFixture[str],
    ) -> None:
        rc = cli.main(["list-cvars", "--category", "no_such_category"])
        assert rc == cli.EXIT_SUCCESS
        # No matches → message goes to stderr, exit is still 0.
        err = capsys.readouterr().err
        assert "no_such_category" in err

    def test_json_format_is_parseable(
        self, capsys: pytest.CaptureFixture[str],
    ) -> None:
        rc = cli.main(["list-cvars", "--format", "json"])
        assert rc == cli.EXIT_SUCCESS
        data = json.loads(capsys.readouterr().out)
        assert isinstance(data, list)
        assert len(data) == 24
        names = {entry["name"] for entry in data}
        assert "sv_fps" in names

    def test_secret_marker_in_text_output(
        self, capsys: pytest.CaptureFixture[str],
    ) -> None:
        rc = cli.main(["list-cvars", "--category", "identity"])
        assert rc == cli.EXIT_SUCCESS
        out = capsys.readouterr().out
        # rconpassword and g_password are secret in v0.1.0.
        rcon_line = next(ln for ln in out.splitlines() if "rconpassword" in ln)
        assert "[SECRET]" in rcon_line


