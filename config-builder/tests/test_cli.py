"""Unit tests for the CLI dispatcher.

Tests call ``cli.main([...])`` directly with explicit argv, so they
don't shell out and don't pay the GUI import cost. The no-subcommand
GUI default path is intentionally not unit-tested — it's tkinter
mainloop, exercised manually and on CI via the smoke import.
"""
from __future__ import annotations

import json
from pathlib import Path

import pytest

from vanguard_config_builder import cli


# ---------------------------------------------------------------------------
# generate
# ---------------------------------------------------------------------------
class TestGenerate:
    def test_writes_a_cfg_file(self, tmp_path: Path) -> None:
        out = tmp_path / "server.cfg"
        rc = cli.main(["generate", "--profile", "cup", "-o", str(out)])
        assert rc == cli.EXIT_SUCCESS
        text = out.read_text(encoding="utf-8")
        assert "Generated by Config Builder" in text
        assert "Cup / Tournament" in text  # profile_name in header
        assert "seta sv_fps" in text       # archived cvar

    def test_no_header_skips_metadata_block(self, tmp_path: Path) -> None:
        out = tmp_path / "server.cfg"
        rc = cli.main([
            "generate", "--profile", "public",
            "-o", str(out), "--no-header",
        ])
        assert rc == cli.EXIT_SUCCESS
        assert "Generated by Config Builder" not in out.read_text()

    def test_strict_default_on(self, tmp_path: Path) -> None:
        # The bundled profiles all validate clean, so --strict default-on
        # must not block the write. This is the contract that the
        # default profiles ship in a state CI can use.
        out = tmp_path / "server.cfg"
        rc = cli.main(["generate", "--profile", "scrim", "-o", str(out)])
        assert rc == cli.EXIT_SUCCESS
        assert out.is_file()

    def test_unknown_profile_returns_io_error(self, tmp_path: Path) -> None:
        out = tmp_path / "server.cfg"
        rc = cli.main([
            "generate", "--profile", "totally_made_up", "-o", str(out),
        ])
        assert rc == cli.EXIT_IO
        assert not out.exists()

    def test_unwritable_path_returns_io_error(self, tmp_path: Path) -> None:
        out = tmp_path / "no_such_dir" / "server.cfg"  # parent dir absent
        rc = cli.main(["generate", "--profile", "cup", "-o", str(out)])
        assert rc == cli.EXIT_IO


