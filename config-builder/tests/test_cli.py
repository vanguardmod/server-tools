class TestDiff:
    def test_identical_files_emit_no_differences(
        self, tmp_path: Path, capsys: pytest.CaptureFixture[str],
    ) -> None:
        cfg = tmp_path / "a.cfg"
        cli.main(["generate", "--profile", "cup", "-o", str(cfg)])
        capsys.readouterr()

        rc = cli.main(["diff", str(cfg), str(cfg)])
        captured = capsys.readouterr()
        assert rc == cli.EXIT_SUCCESS
        assert "no differences" in captured.out

    def test_changed_value_shows_arrow(
        self, tmp_path: Path, capsys: pytest.CaptureFixture[str],
    ) -> None:
        a = tmp_path / "a.cfg"
        b = tmp_path / "b.cfg"
        a.write_text('seta sv_fps "20"\n', encoding="utf-8")
        b.write_text('seta sv_fps "40"\n', encoding="utf-8")

        rc = cli.main(["diff", str(a), str(b)])
        out = capsys.readouterr().out
        assert rc == cli.EXIT_SUCCESS
        assert "~ sv_fps" in out
        assert "20" in out
        assert "40" in out

    def test_added_and_removed_lines(
        self, tmp_path: Path, capsys: pytest.CaptureFixture[str],
    ) -> None:
        a = tmp_path / "a.cfg"
        b = tmp_path / "b.cfg"
        a.write_text('seta sv_fps "20"\n', encoding="utf-8")
        b.write_text('seta sv_fps "20"\nseta g_antilag "1"\n',
                     encoding="utf-8")

        rc = cli.main(["diff", str(a), str(b)])
        out = capsys.readouterr().out
        assert rc == cli.EXIT_SUCCESS
        assert "+ g_antilag" in out

    def test_missing_file_returns_io_error(
        self, tmp_path: Path,
    ) -> None:
        rc = cli.main(["diff", str(tmp_path / "nope.cfg"),
                       str(tmp_path / "also_nope.cfg")])
        assert rc == cli.EXIT_IO


